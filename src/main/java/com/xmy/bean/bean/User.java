package com.xmy.bean.bean;

import java.io.Serializable;

/**
 * @Description:
 * @Author: xumengyang
 * @Date: Created in 13:58 2018/3/2
 */
public class User implements Serializable {

    private Integer id;
    private String nickname;
    private String username;
    private String password;
    private Integer sex;
    private String headPic;
    private String backgroundPic;
    private String backgroundAlt;
    private String email;
    private String phone;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Integer getSex() {
        return sex;
    }

    public void setSex(Integer sex) {
        this.sex = sex;
    }

    public String getHeadPic() {
        return headPic;
    }

    public void setHeadPic(String headPic) {
        this.headPic = headPic;
    }

    public String getBackgroundPic() {
        return backgroundPic;
    }

    public void setBackgroundPic(String backgroundPic) {
        this.backgroundPic = backgroundPic;
    }

    public String getBackgroundAlt() {
        return backgroundAlt;
    }

    public void setBackgroundAlt(String backgroundAlt) {
        this.backgroundAlt = backgroundAlt;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
